#import "UIViewController+MGSplitViewController.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation UIViewController (MGSplitViewController)

////////////////////////////////////////////////////////////////////////////////////////////////////
- (MGSplitViewController *) mgSplitViewController
{
  MGSplitViewController *svc = nil;
  UIView *view = self.view;
  do {
    id nextResponder = [view nextResponder];
    if ([nextResponder isKindOfClass:[MGSplitViewController class]]) {
      svc = nextResponder;
      break;
    }
    view = view.superview;
  } while (view);
  
  return svc;
}

@end
